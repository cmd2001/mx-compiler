package Util;

public class Type {
    private boolean isInt = false, isBool = false, isString = false, isNull = false, isVoid = false, isClass = false;
    private String className = "";
    public boolean isInt() {
        return isInt;
    }
    public boolean isBool() {
        return isBool;
    }
    public boolean isString() {
        return isString;
    }
    public boolean isNull() {
        return isNull;
    }
    public boolean isVoid() {
        return isVoid;
    }
    public boolean isClass() {
        return isClass;
    }
    public String className() {
        return className;
    }
}
